def _build_data_block(
    chart_data: dict,
    person_name: str = "",
    user_context: Optional[str] = None,
) -> str:
    header = chart_data.get("header") or "(данные не получены)"
    houses = _format_table(chart_data.get("houses"))
    planets = _format_table(chart_data.get("planets"))
    house_comparison = _format_table(chart_data.get("house_comparison"))
    aspects = _format_table(chart_data.get("aspects"))

    name_part = f"\n\nИмя: {person_name}" if person_name else ""
    context_part = (
        f"\n\nКОНТЕКСТ ОТ ЧЕЛОВЕКА О ПРОШЕДШЕМ ГОДЕ "
        f"(дополнительный ориентир, не источник фактов карты):\n{user_context}"
        if user_context
        else ""
    )

    return f"""{name_part}{context_part}

ОБЩИЕ ДАННЫЕ:
{header}

ДОМА СОЛЯРА (куспиды и в каком натальном доме они оказались):
{houses}

ПЛАНЕТЫ СОЛЯРА (в каких знаках и солярных домах):
{planets}

СОВМЕЩЕНИЕ НАТАЛЬНЫХ И СОЛЯРНЫХ ДОМОВ:
{house_comparison}

АСПЕКТЫ (солярные планеты к натальным):
{aspects}"""


def build_solar_prompt(
    chart_data: dict,
    person_name: str = "",
    user_context: Optional[str] = None,
) -> str:
    data_block = _build_data_block(chart_data, person_name, user_context)
    return f"{SYSTEM_INSTRUCTIONS}\n\n---\n\nДАННЫЕ РАСЧЁТА:\n{data_block}"


